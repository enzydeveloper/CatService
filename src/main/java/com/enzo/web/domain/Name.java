package com.enzo.web.domain;


/**
 * Sub element of {@link User}
 * @author enzo
 *
 */
class Name
{
	String first;
	String last;
	
	public String getFirst() {
		return first;
	}
	public void setFirst(String first) {
		this.first = first;
	}
	public String getLast() {
		return last;
	}
	public void setLast(String last) {
		this.last = last;
	}
}